/**
 * Generic helper to handle Supabase responses
 */
async function handleResponse({ data, error }) {
  if (error) {
    console.error('Supabase Error:', error);
    throw new Error(error.message);
  }
  return data;
}

const db = {
  // Common internal helpers
  _readLocal() {
    if (!fs.existsSync(DB_JSON_PATH)) return { ...initialData };
    try {
      return JSON.parse(fs.readFileSync(DB_JSON_PATH, 'utf-8'));
    } catch (e) {
      console.error('Error reading local JSON:', e);
      return { ...initialData };
    }
  },

  _writeLocal(data) {
    try {
      fs.writeFileSync(DB_JSON_PATH, JSON.stringify(data, null, 2), 'utf-8');
    } catch (e) {
      console.error('Error writing local JSON:', e);
    }
  },

  async get(table, id = null, filters = {}) {
    if (useSupabase) {
      let query = supabase.from(table).select('*');
      if (id) {
        const result = await query.eq('id', id).single();
        if (result.error && result.error.code === 'PGRST116') {
          await this.seedIfEmpty();
          return await handleResponse(await supabase.from(table).select('*').eq('id', id).single());
        }
        return await handleResponse(result);
      }
      for (const [key, value] of Object.entries(filters)) {
        query = query.eq(key, value);
      }
      const result = await query;
      if (result.data && result.data.length === 0 && Object.keys(filters).length === 0) {
        await this.seedIfEmpty();
        return await handleResponse(await supabase.from(table).select('*'));
      }
      return await handleResponse(result);
    } else {
      const data = this._readLocal();
      const localTable = table === 'press_releases' ? 'pressReleases' : 
                         table === 'map_data' ? 'mapData' :
                         table === 'regional_councils' ? 'regionalCouncils' : table;
      let list = data[localTable] || [];
      if (id) return list.find(i => i.id === id);
      for (const [key, value] of Object.entries(filters)) {
        list = list.filter(i => i[key] === value);
      }
      return list;
    }
  },

  async insert(table, item) {
    if (useSupabase) {
      return await handleResponse(await supabase.from(table).insert(item).select().single());
    } else {
      const data = this._readLocal();
      const localTable = table === 'press_releases' ? 'pressReleases' : 
                         table === 'map_data' ? 'mapData' :
                         table === 'regional_councils' ? 'regionalCouncils' : table;
      if (!data[localTable]) data[localTable] = [];
      const newItem = { id: item.id || Date.now().toString(), ...item };
      data[localTable].push(newItem);
      this._writeLocal(data);
      return newItem;
    }
  },

  async update(table, id, item) {
    if (useSupabase) {
      return await handleResponse(await supabase.from(table).update(item).eq('id', id).select().single());
    } else {
      const data = this._readLocal();
      const localTable = table === 'press_releases' ? 'pressReleases' : 
                         table === 'map_data' ? 'mapData' :
                         table === 'regional_councils' ? 'regionalCouncils' : table;
      const index = data[localTable].findIndex(i => i.id === id);
      if (index === -1) throw new Error('Not found');
      data[localTable][index] = { ...data[localTable][index], ...item, id };
      this._writeLocal(data);
      return data[localTable][index];
    }
  },

  async delete(table, id) {
    if (useSupabase) {
      const { error } = await supabase.from(table).delete().eq('id', id);
      if (error) throw new Error(error.message);
      return { success: true };
    } else {
      const data = this._readLocal();
      const localTable = table === 'press_releases' ? 'pressReleases' : 
                         table === 'map_data' ? 'mapData' :
                         table === 'regional_councils' ? 'regionalCouncils' : table;
      data[localTable] = data[localTable].filter(i => i.id !== id);
      this._writeLocal(data);
      return { success: true };
    }
  },

  async getSettings() {
    if (useSupabase) {
      const res = await supabase.from('settings').select('value').eq('key', 'global').maybeSingle();
      if (!res.data) {
          await this.seedIfEmpty();
          const retry = await supabase.from('settings').select('value').eq('key', 'global').maybeSingle();
          return retry.data ? retry.data.value : initialData.settings;
      }
      return res.data.value;
    } else {
      const data = this._readLocal();
      return data.settings || initialData.settings;
    }
  },

  async updateSettings(value) {
    if (useSupabase) {
      return await handleResponse(await supabase.from('settings').upsert({ key: 'global', value }).select().single());
    } else {
      const data = this._readLocal();
      data.settings = value;
      this._writeLocal(data);
      return { key: 'global', value };
    }
  },

  async getUserByUsername(username) {
    if (useSupabase) {
      const res = await supabase.from('users').select('*').eq('username', username).maybeSingle();
      if (!res.data) {
         await this.seedIfEmpty();
         const retry = await supabase.from('users').select('*').eq('username', username).maybeSingle();
         return retry.data;
      }
      return res.data;
    } else {
      const data = this._readLocal();
      return data.users.find(u => u.username === username);
    }
  },

  async seedIfEmpty() {
    if (!useSupabase) return;
    const { count, error } = await supabase.from('users').select('*', { count: 'exact', head: true });
    if (error) {
        console.error('Seeding check error:', error);
        return;
    }

    if (count === 0) {
      console.log('Database is empty. Starting seeding...');
      try {
        await supabase.from('users').insert(initialData.users);
        await supabase.from('events').insert(initialData.events);
        await supabase.from('press_releases').insert(initialData.pressReleases);
        await supabase.from('announcements').insert(initialData.announcements);
        await supabase.from('photos').insert(initialData.photos);
        await supabase.from('videos').insert(initialData.videos);
        await supabase.from('documents').insert(initialData.documents);
        await supabase.from('settings').upsert({ key: 'global', value: initialData.settings });
        await supabase.from('councils').insert(initialData.councils);
        await supabase.from('map_data').insert(initialData.mapData);
        console.log('Seeding completed successfully.');
      } catch (err) {
        console.error('Seeding failed:', err);
      }
    }
  }
};

module.exports = { supabase, db, initialData };
